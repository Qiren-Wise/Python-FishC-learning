#利用欧几里得算法查找最大公约数
'''
1997 / 615 = 3 ..... 152
615 / 152 = 4 ...... 7
152 / 7 = 21 ....... 5
7 / 5 = 1 .......... 2
5 / 2 = 2 .......... 1
2 / 1 = 2 .......... 0
被除数是他们新的除数，余数是它们新的被除数
'''
def gcd(x,y):
    while y:        #这里运用替换的规则
        i = x%y     #先把x除y的余数放在公共参数i里面     
        x = y       #再把被除数放在除数位置上
        y = i       #最后把余数放在被除数的位置上
    return x    

print(gcd(12,16))
